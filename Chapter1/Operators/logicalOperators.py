# "and" --> Returns True if both statements are true
# "Or" --> Return True if one of the both statements is true
# "Not" --> Return the result, return False if the result is true 

exp1 = 2 > 1 # T
exp2 = 5 < 4 # F
print("Exp1 and Exp2: ", exp1 and exp2)
print("Exp1 or Exp2: ", exp1 or exp2)
print("notExp1 :", not(exp1))